              <br><br>
              <p>&emsp;&emsp;Saat kita mendengar kata remaja atau anak muda, sejenak yang terlintas di benak kita adalah seseorang yang memiliki kebebasan, masanya untuk bermain, mencari jati diri, bergaul, bahkan juga dikaitkan dengan kenakalan remaja, narkoba, perilaku seksual berisiko dan berbagai macam masalah remaja lainnya. Di satu sisi, anak muda Indonesia juga memiliki beragam prestasi yang mengharumkan nama bangsa, mulai dari juara olimpiade sains di tingkat internasional, satu – satunya pengisi acara termuda di Grammy Awards 2016, sebagai pengembang bisnis berbasis teknologi yang sukses besar, dan lainnya. Berbagai macam gejolak remaja dan potensi yang ada dalam diri remaja Indonesia, sangat disayangkan apabila tidak dimanfaatkan untuk menjawab kebutuhan remaja itu sendiri dalam mengatasi permasalahan yang ada.
              <br><br>
              &emsp;&emsp;Berbagai kebijakan, strategi, dan program telah dirancang sangat baik oleh pemerintah untuk mengatasi permasalahan yang dihadapi oleh remaja Indonesia, termasuk dalam mengatasi isu kesehatan reproduksi. Namun, hasilnya belum optimal. Salah satu  faktor penyebabnya yaitu kurangnya keterlibatan atau partisipasi anak muda yang bemakna dalam upaya mengatasi permasalahan tersebut.
              <br><br>
              Mengapa Anak Muda Perlu Dilibatkan?
              <br>
                <ol>
                    <li>Alasan demografi : youth Bulge!
                    Indonesia mengalami bonus demografi yang terjadi hingga tahun 2050, dimana penduduk usia produktif termasuk golongan remaja mencapai hampir 70%. Besarnya jumlah anak muda menjadi tantangan dan sekaligus investasi. Partisipasi anak muda yang bermakna dapat mendukung penguatan kemampuan dan daya kritis anak muda untuk memenuhi kebutuhan anak muda itu sendiri.</li>
                    <br>
                    <li>Membangun efektivitas program
                    Melibatkan anak muda secara bermakna juga melibatkan mereka sebagai subyek dalam menjalankan program bukan hanya sebagai objek atau penerima manfaat. Dengan itu, anak muda dapat mengasah kepekaan terhadap hak dan kewajiban sebagai warga Negara, membuat kebijakan tepat sasaran dan sesuai dengan kebutuhan anak muda. Suatu program yang dapat menjawab kebutuhan anak muda, dapat secara efektif mencapai tujuan dan dampak yang diharapkan.</li>
                    <br>
                    <li>Membangun lingkungan yang positif antara anak muda – orang dewasa
                    Kemitraan yang terbangun dengan menekankan pada proses saling membangun kekuatan antara anak muda – orang dewasa dapat mengubah nilai dan norma di masyarakat yang memandang anak muda sebagai pihak yang tidak berdaya atau mengubah pandangan bahwa orang dewasa tidak bisa bermitra secara positif dengan anak muda.</li>
                </ol>
                <br>
                &emsp;&emsp;Apa itu partisipasi anak muda yang bermakna?
                The U.S. National Commission on Resources for Youth mendefinisikan partisipasi anak muda yang mengarah pada keterlibatan anak muda untuk bertanggung jawab dan melakukan suatu tindakan dengan adanya kesempatan untuk merencanakan dan/atau membuat keputusan yang dapat mempengaruhi orang lain, baik yang di luar atau di dalam partisipasi anak muda itu sendiri.2
                <br><br>
                &emsp;&emsp;Lalu, keterlibatan seperti apa yang bisa disebut sebagai partisipasi yang bermakna?
                <br><br>
                Terdapat berbagai macam model partisipasi yang disajikan oleh para ahli, salah satunya yaitu tangga partisipasi Hart (Hart’s Ladder of Participation). Tangga partisipasi ini dibuat untuk melihat sejauh apa partisipasi anak muda dalam suatu kegiatan tertentu serta sejauh apa kolaborasi anak muda bersama orang dewasa. Beberapa tingkatan partisipasi anak muda yang disebut bermakna, yaitu :
                <br><br>
                <ol>
                    <li>Youth initiated – shared decisions (inisiatif anak muda dan diputuskan bersama dengan orang dewasa)
                    Kegiatan diinisiasi dan dijalankan oleh anak muda, baik dari perencanaan sampai pelaksanaan. Kegiatan juga dikonsultasikan kepada orang dewasa jika dibutuhkan. Orang dewasa berperan untuk memberikan saran.</li>
                    <br>
                    <li>Youth initiated and directed (inisiatif anak muda, tetapi diarahkan oleh orang dewasa)
                    Kegiatan diinisiasi (dibuat) oleh anak muda dan orang dewasa membantu mengarahkan kegiatan.</li>
                    <br>
                    <li>Consulted and informed (dikonsultasi dan memahami)
                    Kegiatan dijalankan oleh orang dewasa tetapi memperhatikan pendapat dari anak muda bahkan diterapkan dalam menjalankan kegiatan tersebut.</li>
                    <br>
                    <li>Assigned but informed (ditugaskan tetapi memahami)
                    Anak muda memahami tujuan dari kegiatan yang akan dilakukan, siapa yang akan melakukan pengambilan keputusan dan mengapa, serta memutuskan sendiri keikutsertakaan dalam kegiatan tersebut.</li>
                </ol>
                <br>
                &emsp;&emsp;Namun, terdapat beberapa jenis partisipasi anak muda yang tidak bermakna atau non partisipatif, yang mungkin sering terjadi dalam menjalankan program anak muda yaitu :
                <br><br>
                <ol>
                    <li>Tokenism
                    Anak muda memberikan pendapat, tetapi kenyataannya sedikit atau bahkan tidak ada kesempatan atau pilihan – pilihan sesuai dengan kebutuhannya anak muda yang sebenarnya.</li>
                    <br>
                    <li>Decoration
                    Anak muda dilibatkan hanya untuk pemanis dan penggembira untuk kepentingan orang dewasa walaupun kegiatan tersebut adalah kegiatan anak muda.</li>
                    <br>
                    <li>Manipulation
                    Pada tahap ini, anak muda tidak memahami isu yang diangkat dan apa tujuan yang mereka lakukan. Contohnya adalah anak muda diminta untuk memegang poster, menyebarkan flyer terhadap isu tertentu namun tidak mengetahui makna aksi tersebut dan mengapa mereka melakukannya.</li>
                </ol>
                <br><br>
                <h4>Faktor – faktor yang berkontribusi terhadap partisipasi remaja ideal</h4>
                <br>
                Faktor yang memberikan pengaruh terhadap partisipasi anak muda terdiri dari :
                <br>
                <ol>
                    <li>Kekuatan
                    Faktor yang dapat mendorong dan membantu terwujudnya partisipasi anak muda yang bermakna yaitu munculnya insiatif pemerintah dan anak muda untuk mengatasi permasalahan anak muda, pengembangan kapasitas organisasi pemuda melalui capacity building yang rutin dilaksanakan, serta diberikannya kesempatan bagi anak muda oleh pemerintah untuk berpartisipasi dalam pengembangan kebijakan terkait anak muda, mulai dari perencanaan hingga evaluasi yang disahkan melalui undang – undang.</li>
                    <br>
                    <li>Kelemahan
                    Belum adanya kesadaran mengenai pentingnya partisipasi anak muda yang bermakna, baik dari pemerintah ataupun orang dewasa, hingga anak muda itu sendiri, adanya stigma bahwa anak muda tidak memiliki kapasitas yang memadai untuk bekerjasama dengan orang dewasa, belum adanya kebijakan yang khusus dalam mengatur partisipasi anak muda dalam pembuatan kebijakan maupun pengambilan keputusan, serta minimnya penilaian yang dilakukan dalam menilai partisipasi anak muda yang bermakna dalam pengembangan program atau keerlibatan dalam pengambilan keputusan.</li>
                </lo>
                <br>
                <h4>Rekomendasi</h4>
                <br>
                <ol>
                    <li>Kondisi ideal partisipasi anak muda yang bermakna
                Kondisi ideal yang sesuai dengan konteks Indonesia dalam menerapkan partisipasi anak muda yang bermakna yaitu adanya kerjasama maupun kolaborasi antara orang dewasa dan anak muda. Dalam menjalankan program atau kegiatan, anak muda dan orang dewasa idealnya membagi peran dan tanggung jawab sesuai dengan kemampuannya masing – masing. Tidak dapat dipungkiri bahwa anak muda masih memerlukan asistensi orang dewasa yang memiliki lebih banyak pengetahuan dan pengalaman dalam menjalankan program dan kegiatan.</li>
                <br>
                    <li>Peningkatan kapasitas terkait partisipasi anak muda yang bermakna
                Berdasarkan faktor kelemahan, dimana anak muda tidak memiliki kapasitas yan memadai untuk menjalankan program atau aktivitas sesuai dengan orang dewasa, maka peningkatan kapasitas sangat penting untuk dilakukan. Kapasitas yang dapat diberikan pada anak muda atau organisasi anak muda terkait dengan implementasi program yaitu manajemen organisasi, pelaporan keuangan, etika kemitraan dalam program, leadership, advokasi, kepercayaan diri, dan kemampuan sosial lainnya yang dibutuhkan dalam implementasi partisipasi yang bermakna.</li>
