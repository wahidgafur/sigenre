              <h1><b>Cegah stunting pada anak</b></h1>
                <p>Rabu, 29 Juni 2022</p>
              <Hr></Hr>
              <div align=justify>
              <img src="assets/img/berita/Berita2.png" width=75% />
              <br><br>
              <p>&emsp;&emsp;Sebagian besar masyarakat mungkin belum memahami istilah yang disebut stunting. Stunting adalah masalah kurang gizi kronis yang disebabkan oleh kurangnya asupan gizi dalam waktu yang cukup lama, sehingga mengakibatkan gangguan pertumbuhan pada anak yakni tinggi badan anak lebih rendah atau pendek (kerdil) dari standar usianya.
              <br><br>
              &emsp;&emsp;Kondisi tubuh anak yang pendek seringkali dikatakan sebagai faktor keturunan (genetik) dari kedua orang tuanya, sehingga masyarakat banyak yang hanya menerima tanpa berbuat apa-apa untuk mencegahnya. Padahal seperti kita ketahui, genetika merupakan faktor determinan kesehatan yang paling kecil pengaruhnya bila dibandingkan dengan faktor perilaku, lingkungan (sosial, ekonomi, budaya, politik), dan pelayanan kesehatan. Dengan kata lain, stunting merupakan masalah yang sebenarnya bisa dicegah.
              <br><br>
              &emsp;&emsp;Salah satu fokus pemerintah saat ini adalah pencegahan stunting. Upaya ini bertujuan agar anak-anak Indonesia dapat tumbuh dan berkembang secara optimal dan maksimal, dengan disertai kemampuan emosional, sosial, dan fisik yang siap untuk belajar, serta mampu berinovasi dan berkompetisi di tingkat global.
              <br><br>
              &emsp;&emsp;“Terdapat tiga hal yang harus diperhatikan dalam pencegahan stunting, yaitu perbaikan terhadap pola makan, pola asuh, serta perbaikan sanitasi dan akses air bersih”, tutur Menteri Kesehatan RI, Nila Farid Moeloek, di Jakarta (7/4).
              <br><br>
              &emsp;&emsp;Diterangkan Menkes Nila Moeloek, kesehatan berada di hilir. Seringkali masalah-masalah non kesehatan menjadi akar dari masalah stunting, baik itu masalah ekonomi, politik, sosial, budaya, kemiskinan, kurangnya pemberdayaan perempuan, serta masalah degradasi lingkungan. Karena itu, ditegaskan oleh Menkes, kesehatan membutuhkan peran semua sektor dan tatanan masyarakat.
              <br>
              <br>
                <ol>
                    <li>Pola Makan

                Masalah stunting dipengaruhi oleh rendahnya akses terhadap makanan dari segi jumlah dan kualitas gizi, serta seringkali tidak beragam.

                Istilah “Isi Piringku” dengan gizi seimbang perlu diperkenalkan dan dibiasakan dalam kehidupan sehari-hari. Bagi anak-anak dalam masa pertumbuhan, memperbanyak sumber protein sangat dianjurkan, di samping tetap membiasakan mengonsumsi buah dan sayur.

                Dalam satu porsi makan, setengah piring diisi oleh sayur dan buah, setengahnya lagi diisi dengan sumber protein (baik nabati maupun hewani) dengan proporsi lebih banyak daripada karbohidrat.</li>
                <br> <br>
             
                <li>Pola Asuh

                Stunting juga dipengaruhi aspek perilaku, terutama pada pola asuh yang kurang baik dalam praktek pemberian makan bagi bayi dan Balita.

                Dimulai dari edukasi tentang kesehatab reproduksi dan gizi bagi remaja sebagai cikal bakal keluarga, hingga para calon ibu memahami pentingnya memenuhi kebutuhan gizi saat hamil dan stimulasi bagi janin, serta memeriksakan kandungan empat kali selama kehamilan.

                Bersalin di fasilitas kesehatan, lakukan inisiasi menyusu dini (IMD) dan berupayalah agar bayi mendapat colostrum air susu ibu (ASI). Berikan hanya ASI saja sampai bayi berusia 6 bulan.

                Setelah itu, ASI boleh dilanjutkan sampai usia 2 tahun, namun berikan juga makanan pendamping ASI. Jangan lupa pantau tumbuh kembangnya dengan membawa buah hati ke Posyandu setiap bulan.

                Hal lain yang juga perlu diperhatikan adalah berikanlah hak anak mendapatkan kekebalan dari penyakit berbahaya melalui imunisasi yang telah dijamin ketersediaan dan keamanannya oleh pemerintah. Masyarakat bisa memanfaatkannya dengan tanpa biaya di Posyandu atau Puskesmas.</li>
                <br> <br>
                <li>Sanitasi dan Akses Air Bersih

                Rendahnya akses terhadap pelayanan kesehatan, termasuk di dalamnya adalah akses sanitasi dan air bersih, mendekatkan anak pada risiko ancaman penyakit infeksi. Untuk itu, perlu membiasakan cuci tangan pakai sabun dan air mengalir, serta tidak buang air besar sembarangan.</li>
                </ol>
